const a = 2;

switch (a) {
    case 1: {
        const result = a * 2;
        console.log("1", result);
        break;
    }
    case 2: {
        const result = a / 2;
        console.log("2", result);
        break;
    }
    case 3: {
        const result = a + 2;
        console.log("3", result);
        break;
    }
}